package medium

import (
	"reflect"
	"testing"
)

func TestSmallestTrimmedNumbers(t *testing.T) {
	cases := []struct {
		nums     []string
		queries  [][]int
		expected []int
	}{
		{[]string{"102", "473", "251", "814"}, [][]int{{1, 1}, {2, 3}, {4, 2}, {1, 2}}, []int{2, 2, 1, 0}},
		{[]string{"24", "37", "96", "04"}, [][]int{{2, 1}, {2, 2}}, []int{3, 0}},
		{[]string{"000", "000", "000", "000"}, [][]int{{1, 1}, {2, 1}, {3, 1}, {4, 1}}, []int{0, 1, 2, 3}},
		{[]string{"0", "0", "0", "0"}, [][]int{{1, 1}, {2, 1}, {3, 1}, {4, 1}}, []int{0, 1, 2, 3}},
		{[]string{"123456789", "987654321", "111111111", "000000000"}, [][]int{{1, 9}, {2, 9}, {3, 9}, {4, 9}}, []int{3, 2, 0, 1}},
		{[]string{"9", "8", "7", "6", "5", "4", "3", "2", "1", "0"}, [][]int{{1, 1}, {5, 1}, {10, 1}}, []int{9, 5, 0}},
		{[]string{"100", "200", "300", "400", "500"}, [][]int{{1, 2}, {2, 2}, {3, 2}, {4, 2}, {5, 2}}, []int{0, 1, 2, 3, 4}},
		{[]string{"00100", "00101", "00110", "00111", "01000"}, [][]int{{1, 3}, {2, 3}, {3, 3}, {4, 3}, {5, 3}}, []int{4, 0, 1, 2, 3}},
		{[]string{"99999", "99998", "99997", "99996", "99995"}, [][]int{{1, 5}, {2, 5}, {3, 5}, {4, 5}, {5, 5}}, []int{4, 3, 2, 1, 0}},
		{[]string{"00001", "00010", "00100", "01000", "10000"}, [][]int{{1, 5}, {2, 5}, {3, 5}, {4, 5}, {5, 5}}, []int{0, 1, 2, 3, 4}},
		{[]string{"123", "123", "123", "123", "123"}, [][]int{{1, 3}, {2, 3}, {3, 3}, {4, 3}, {5, 3}}, []int{0, 1, 2, 3, 4}},
		{[]string{"000", "001", "002", "010", "100"}, [][]int{{1, 3}, {2, 3}, {3, 3}, {4, 3}, {5, 3}}, []int{0, 1, 2, 3, 4}},
		{[]string{"9876543210", "8765432109", "7654321098", "6543210987", "5432109876"}, [][]int{{1, 10}, {3, 10}, {5, 10}}, []int{4, 2, 0}},
		{[]string{"1", "10", "100", "1000", "10000"}, [][]int{{1, 5}, {2, 5}, {3, 5}, {4, 5}, {5, 5}}, []int{0, 1, 2, 3, 4}},
		{[]string{"999", "998", "997", "996", "995", "994", "993", "992", "991", "990"}, [][]int{{1, 3}, {5, 3}, {10, 3}}, []int{9, 5, 0}},
		{[]string{"0000000000", "0000000001", "0000000010", "0000000100", "0000001000"}, [][]int{{1, 10}, {2, 10}, {3, 10}, {4, 10}, {5, 10}}, []int{0, 1, 2, 3, 4}},
		{[]string{"1111111111", "2222222222", "3333333333", "4444444444", "5555555555"}, [][]int{{1, 10}, {2, 10}, {3, 10}, {4, 10}, {5, 10}}, []int{0, 1, 2, 3, 4}},
		{[]string{"1000000000", "999999999", "999999998", "999999997", "999999996"}, [][]int{{1, 10}, {2, 10}, {3, 10}}, []int{0, 4, 3}},
		{[]string{"000", "001", "010", "100", "999"}, [][]int{{1, 3}, {2, 3}, {3, 3}, {4, 3}, {5, 3}}, []int{0, 1, 2, 3, 4}},
		{[]string{"000000", "000001", "000010", "000100", "001000", "010000", "100000"}, [][]int{{1, 6}, {3, 6}, {5, 6}, {7, 6}}, []int{0, 2, 4, 6}},
	}
	for _, c := range cases {
		t.Run("", func(t *testing.T) {
			result := SmallestTrimmedNumbers(c.nums, c.queries)
			if !reflect.DeepEqual(result, c.expected) {
				t.Errorf("SmallestTrimmedNumbers(%q,%v) == %v, expected %v", c.nums, c.queries, result, c.expected)
			}

		})

	}
}
